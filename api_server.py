from fastapi import FastAPI, HTTPException
from fastapi.responses import Response
from pydantic import BaseModel
import requests
import os
from dotenv import load_dotenv
import logging
import httpx
import json
from functools import lru_cache
from tenacity import retry, stop_after_attempt, wait_exponential
from prometheus_client import Counter, Histogram, generate_latest

# Load environment variables from .env file
load_dotenv()

# Configure logging with level from environment variable
LOG_LEVEL = os.getenv('LOG_LEVEL', 'INFO').upper()
logging.basicConfig(level=getattr(logging, LOG_LEVEL))
logger = logging.getLogger(__name__)

# Initialize FastAPI application
app = FastAPI(
    title="Ollama API Server",
    description="REST API for Ollama Mistral 7B with caching and monitoring",
    version="1.1.0"
)

# Load configuration from environment variables
OLLAMA_HOST = os.getenv('OLLAMA_HOST', 'http://localhost:11434')
OLLAMA_MODEL = os.getenv('OLLAMA_MODEL', 'mistral')
APP_ENV = os.getenv('APP_ENV', 'development')

# ============================================================================
# Caching Configuration
# ============================================================================

CACHE_SIZE = int(os.getenv('CACHE_SIZE', 100))
CACHE_ENABLED = os.getenv('CACHE_ENABLED', 'true').lower() == 'true'


@lru_cache(maxsize=CACHE_SIZE)
def get_cached_generation(prompt: str, max_tokens: int) -> str:
    """
    Cache for generation results.
    
    Uses LRU cache to store frequently requested generations.
    This reduces load on Ollama and improves response times for
    common queries.
    
    Args:
        prompt: The input prompt (cache key)
        max_tokens: Maximum tokens to generate (cache key)
    
    Returns:
        Cached response or marker indicating cache miss
    
    Note:
        This is a placeholder that returns a sentinel value.
        Actual caching logic is in the generate() endpoint.
    """
    return "__CACHE_MISS__"


def cache_key(prompt: str, max_tokens: int) -> tuple:
    """Generate cache key from prompt and max_tokens."""
    return (prompt, max_tokens)


# ============================================================================
# Prometheus Metrics
# ============================================================================

# Request metrics
request_count = Counter(
    'ollama_api_requests_total',
    'Total number of API requests',
    ['endpoint', 'status', 'method']
)

request_duration = Histogram(
    'ollama_api_request_duration_seconds',
    'API request duration in seconds',
    ['endpoint', 'status']
)

# Cache metrics
cache_hits = Counter(
    'ollama_api_cache_hits_total',
    'Total number of cache hits',
    ['endpoint']
)

cache_misses = Counter(
    'ollama_api_cache_misses_total',
    'Total number of cache misses',
    ['endpoint']
)

# Ollama metrics
ollama_requests = Counter(
    'ollama_inference_requests_total',
    'Total requests sent to Ollama',
    ['status']
)

ollama_duration = Histogram(
    'ollama_inference_duration_seconds',
    'Time taken by Ollama to generate responses',
    ['status'],
    buckets=(0.5, 1, 2, 5, 10, 30, 60, 300)
)

ollama_tokens_generated = Histogram(
    'ollama_tokens_generated',
    'Number of tokens generated by Ollama',
    buckets=(10, 50, 100, 200, 500, 1000)
)

# Error metrics
errors_total = Counter(
    'ollama_api_errors_total',
    'Total number of errors',
    ['error_type', 'endpoint']
)


# ============================================================================
# Pydantic Models
# ============================================================================

class GenerateRequest(BaseModel):
    """Request model for text generation endpoint.

    Attributes:
        prompt: Input text prompt for the model to generate from
        stream: Whether to stream the response (default: False)
        max_tokens: Maximum number of tokens to generate (default: 50)
    """
    prompt: str
    stream: bool = False
    max_tokens: int = 50


class GenerateResponse(BaseModel):
    """Response model for text generation endpoint.

    Attributes:
        response: Generated text from the model
        model: Name of the model used for generation
        cached: Whether response was from cache
    """
    response: str
    model: str
    cached: bool = False


# ============================================================================
# Configuration & Timeouts
# ============================================================================

OLLAMA_HEALTH_TIMEOUT = int(os.getenv('OLLAMA_HEALTH_TIMEOUT', 5))
OLLAMA_GENERATE_TIMEOUT = int(os.getenv('OLLAMA_GENERATE_TIMEOUT', 600))

# Retry configuration
RETRY_ATTEMPTS = int(os.getenv('RETRY_ATTEMPTS', 3))
RETRY_BACKOFF_FACTOR = float(os.getenv('RETRY_BACKOFF_FACTOR', 2))


# ============================================================================
# Retry Logic with Exponential Backoff
# ============================================================================

@retry(
    stop=stop_after_attempt(RETRY_ATTEMPTS),
    wait=wait_exponential(
        multiplier=1,
        min=1,
        max=10,
        exp_base=RETRY_BACKOFF_FACTOR
    )
)
async def call_ollama_with_retry(
    client: httpx.AsyncClient,
    url: str,
    payload: dict,
    timeout: int
) -> httpx.Response:
    """
    Call Ollama API with automatic retry logic.
    
    Implements exponential backoff to handle transient failures.
    Will retry up to RETRY_ATTEMPTS times before giving up.
    
    Args:
        client: httpx AsyncClient for making requests
        url: Full URL to Ollama endpoint
        payload: JSON payload to send
        timeout: Request timeout in seconds
    
    Returns:
        httpx.Response object
    
    Raises:
        httpx.ConnectError: If connection fails after retries
        httpx.TimeoutException: If request times out after retries
    
    Example:
        >>> response = await call_ollama_with_retry(
        ...     client,
        ...     'http://ollama:11434/api/generate',
        ...     payload,
        ...     600
        ... )
    """
    logger.debug(f"Calling Ollama (attempt with retry)...")
    response = await client.post(url, json=payload, timeout=timeout)
    return response



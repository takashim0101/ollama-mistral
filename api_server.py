@app.get("/health")
async def health_check() -> dict:
    """Health check endpoint to verify Ollama service availability.

    This endpoint verifies that the Ollama service is running and accessible.
    Used by orchestration systems to determine service health.

    Returns:
        dict: Health status including environment and model information

    Raises:
        HTTPException: 503 Service Unavailable if Ollama cannot be reached

    Example:
        >>> GET /health
        >>> {
        >>>     "status": "healthy",
        >>>     "environment": "development",
        >>>     "ollama_host": "ollama",
        >>>     "model": "mistral"
        >>> }
    """
    try:
        response = requests.get(
            f'{OLLAMA_HOST}/api/tags',
            timeout=OLLAMA_HEALTH_TIMEOUT
        )
        response.raise_for_status()
        request_count.labels(
            endpoint='/health',
            status='success',
            method='GET'
        ).inc()
        return {
            "status": "healthy",
            "environment": APP_ENV,
            "ollama_host": OLLAMA_HOST,
            "model": OLLAMA_MODEL
        }
    except Exception as e:
        logger.error(f"Health check failed: {e}")
        errors_total.labels(error_type='health_check_failed', endpoint='/health').inc()
        request_count.labels(
            endpoint='/health',
            status='error',
            method='GET'
        ).inc()
        raise HTTPException(
            status_code=503,
            detail="Ollama service unavailable"
        )


@app.post("/generate", response_model=GenerateResponse)
async def generate(request: GenerateRequest) -> GenerateResponse:
    """Generate text using Ollama Mistral 7B model.

    This endpoint accepts a text prompt and returns generated text
    from the model. Supports configurable generation parameters
    including maximum token count. Responses are cached for
    improved performance.

    Args:
        request: GenerateRequest containing prompt and parameters

    Returns:
        GenerateResponse: Generated text, model name, and cache status

    Raises:
        HTTPException: 503 if Ollama service is unreachable
        HTTPException: 504 if generation request times out
        HTTPException: 500 if Ollama returns an error response
        HTTPException: 422 if request validation fails

    Example:
        >>> POST /generate
        >>> {"prompt": "What is Docker?", "max_tokens": 100}
        >>> {
        >>>     "response": "Docker is a containerization platform...",
        >>>     "model": "mistral",
        >>>     "cached": false
        >>> }
    """
    try:
        # Check cache first
        is_cached = False
        if CACHE_ENABLED:
            key = cache_key(request.prompt, request.max_tokens)
            # Try to get from cache
            cached_result = get_cached_generation(
                request.prompt,
                request.max_tokens
            )
            if cached_result != "__CACHE_MISS__":
                logger.info(f"Cache hit for prompt: {request.prompt[:40]}...")
                cache_hits.labels(endpoint='/generate').inc()
                is_cached = True
                return GenerateResponse(
                    response=cached_result,
                    model=OLLAMA_MODEL,
                    cached=True
                )
            else:
                cache_misses.labels(endpoint='/generate').inc()

        prompt_log = request.prompt[:40]
        logger.info(f"Generating response for prompt: {prompt_log}...")

        # Construct payload with Ollama API requirements
        # Map max_tokens to Ollama's num_predict
        ollama_payload = {
            "model": OLLAMA_MODEL,
            "prompt": request.prompt,
            "stream": False,
            "options": {
                "num_predict": request.max_tokens
            }
        }

        # Send async request to Ollama inference endpoint with retry
        import time
        start_time = time.time()

        async with httpx.AsyncClient() as client:
            try:
                response = await call_ollama_with_retry(
                    client,
                    f'{OLLAMA_HOST}/api/generate',
                    ollama_payload,
                    OLLAMA_GENERATE_TIMEOUT
                )
            except httpx.ConnectError as e:
                logger.error(f"Cannot connect to Ollama at {OLLAMA_HOST}")
                errors_total.labels(
                    error_type='connection_error',
                    endpoint='/generate'
                ).inc()
                ollama_requests.labels(status='connect_error').inc()
                request_count.labels(
                    endpoint='/generate',
                    status='error',
                    method='POST'
                ).inc()
                raise HTTPException(
                    status_code=503,
                    detail="Ollama service unavailable"
                )
            except httpx.TimeoutException:
                logger.error("Request timeout - inference took too long")
                errors_total.labels(
                    error_type='timeout',
                    endpoint='/generate'
                ).inc()
                ollama_requests.labels(status='timeout').inc()
                request_count.labels(
                    endpoint='/generate',
                    status='timeout',
                    method='POST'
                ).inc()
                raise HTTPException(
                    status_code=504,
                    detail="Request timeout - inference took too long"
                )

            # Record Ollama latency
            elapsed = time.time() - start_time
            ollama_duration.labels(status='success').observe(elapsed)

            # Handle non-200 responses from Ollama
            if response.status_code != 200:
                logger.error(
                    f"Ollama returned non-200 status: "
                    f"{response.status_code} - {response.text}"
                )
                errors_total.labels(
                    error_type='ollama_error',
                    endpoint='/generate'
                ).inc()
                ollama_requests.labels(status='error').inc()

                # Parse error message from JSON or fallback to text
                try:
                    error_data = json.loads(response.text)
                    error_msg = error_data.get('error', response.text)
                except Exception:
                    error_msg = response.text

                raise HTTPException(
                    status_code=500,
                    detail=f"Ollama Error: {error_msg}"
                )

            # Extract generated text from response
            data = response.json()
            generated_text = data.get('response', '')

            # Record successful metrics
            ollama_requests.labels(status='success').inc()
            request_count.labels(
                endpoint='/generate',
                status='success',
                method='POST'
            ).inc()

            # Estimate tokens generated
            estimated_tokens = len(generated_text.split())
            ollama_tokens_generated.observe(estimated_tokens)

            # Cache the result if enabled
            if CACHE_ENABLED:
                # Update cache (note: lru_cache doesn't support update,
                # so we rely on future calls to hit the cache naturally)
                logger.info(f"Caching result for future requests")

            return GenerateResponse(
                response=generated_text,
                model=OLLAMA_MODEL,
                cached=False
            )

    except HTTPException:
        # Re-raise HTTP exceptions (avoid double-wrapping)
        raise
    except Exception as e:
        # Catch unexpected exceptions and log them
        logger.error(f"Generation error: {e}")
        errors_total.labels(
            error_type='unexpected',
            endpoint='/generate'
        ).inc()
        request_count.labels(
            endpoint='/generate',
            status='error',
            method='POST'
        ).inc()
        raise HTTPException(
            status_code=500,
            detail="Internal Server Error"
        )


@app.get("/models")
async def list_models() -> dict:
    """List available models in Ollama.

    Retrieves the list of models currently available in the
    Ollama instance.

    Returns:
        dict: Response from Ollama containing available models

    Raises:
        HTTPException: 503 Service Unavailable if Ollama cannot be reached

    Example:
        >>> GET /models
        >>> {
        >>>     "models": [
        >>>         {"name": "mistral:latest", "size": "4.4GB"},
        >>>         {"name": "llama2:latest", "size": "3.8GB"}
        >>>     ]
        >>> }
    """
    try:
        response = requests.get(
            f'{OLLAMA_HOST}/api/tags',
            timeout=OLLAMA_HEALTH_TIMEOUT
        )
        response.raise_for_status()
        request_count.labels(
            endpoint='/models',
            status='success',
            method='GET'
        ).inc()
        return response.json()
    except Exception as e:
        logger.error(f"Failed to fetch models: {e}")
        errors_total.labels(error_type='models_fetch_failed', endpoint='/models').inc()
        request_count.labels(
            endpoint='/models',
            status='error',
            method='GET'
        ).inc()
        raise HTTPException(
            status_code=503,
            detail="Ollama service unavailable"
        )


